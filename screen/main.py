from machine import Pin, SoftI2C

i2c = SoftI2C(sda=Pin(9), scl=Pin(8), freq=400000)

device = 0x3E
b = bytearray(1)

# turn screen on
b[0] = 255
i2c.writeto_mem(device, 0x22, b)

b[0] = 1
i2c.writeto_mem(device, 0x20, b)

# rotate screen
b[0] = 6
i2c.writeto_mem(device, 0x36, b)

command = 0x6a

x_left = 0x00
y_top = 0x00
x_right = 0x44
y_bottom = 0x10

colour = 0x4325


commands = [command, x_left, y_top, x_right, y_bottom, colour]
i2c.writeto_mem(device, command, bytearray(commands))

